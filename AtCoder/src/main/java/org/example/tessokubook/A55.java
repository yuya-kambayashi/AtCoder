    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        TreeSet<Integer> set = new TreeSet<>();
        for (int i = 0; i < n; i++) {
            int t = sc.nextInt();
            if (t == 1) {
                int x = sc.nextInt();
                set.add(x);

            } else if (t == 2) {
                int x = sc.nextInt();
                set.remove(x);

            } else if (t == 3) {
                int x = sc.nextInt();

                Integer y = set.ceiling(x);
                if (y == null) {
                    System.out.println(-1);

                } else {
                    System.out.println(y);
                }
            }
        }
    }
