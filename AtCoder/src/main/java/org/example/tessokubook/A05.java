    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        final int k = sc.nextInt();
        long cnt = 0;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= n; j++) {
                int t = k - i - j;
                if (1 <= t && t <= n) {
                    cnt++;
                }
            }
        }

        System.out.println(cnt);
    }
//}

