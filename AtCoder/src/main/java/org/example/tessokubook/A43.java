        final int l = sc.nextInt();
        int[] aa = new int[n];
        String[] bb = new String[n];
        for (int i = 0; i < n; i++) {
            aa[i] = sc.nextInt();
            bb[i] = sc.next();
        }

        int max = 0;
        for (int i = 0; i < n; i++) {
            if (bb[i].equals("E")) {
                max = Math.max(max, l - aa[i]);
            } else {
                max = Math.max(max, aa[i]);
            }
        }


        System.out.println(max);
