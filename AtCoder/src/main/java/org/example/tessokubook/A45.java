    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        final String b = sc.next();
        String s = sc.next();

        int score = 0;
        for (int i = 0; i < n; i++) {
            char c = s.charAt(i);
            if (c == 'R') {
                score += 2;
            } else if (c == 'B') {
                score += 1;
            }
        }
        score %= 3;
        if (score == 2 && b.equals("R") || score == 1 && b.equals("B") || score == 0 && b.equals("W")) {
            System.out.println("Yes");
        } else {
            System.out.println("No");
        }
    }

