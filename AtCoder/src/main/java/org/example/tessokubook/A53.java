    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        PriorityQueue<Integer> pq = new PriorityQueue<>();
        for (int i = 0; i < n; i++) {
            int t = sc.nextInt();
            if (t == 1) {
                int x = sc.nextInt();
                pq.add(x);

            } else if (t == 2) {
                int x = pq.peek();
                System.out.println(x);

            } else if (t == 3) {
                pq.poll();

            }
        }

        System.out.println(n);
    }
